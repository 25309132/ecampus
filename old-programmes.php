                                        <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom" role="tabpanel" style="display: none;"><p><b> </b></p>The Master of Arts in Social Policy is offered by the School of Arts and Social Sciences
                                        <p>Social policy is one of the academic social sciences and is based on a distinct empirical focus, i.e., support for the wellbeing of citizens provided through social action. In its academic and operational thrust, social policy draws from the methods and materials used by other social sciences in order to ensure the promotion of human welfare and happiness. It is informed by local, national and international desire to produce specialists that can address local as well as global issues that relate to social development, governance and especially, poverty eradication in developing countries.</p>
                                        <p>The programme is founded on the philosophy of the globe as a social and multicultural community. It aims at developing critical thinking and analytical skills offering courses that will lead to careers in research, consultancy, and various aspects of social development. The programme will also provide opportunity to develop skills in research, analysis and interpretation of data information that will address development needs of the 21st Century.
                                    The programme will attract students and practicing professionals interested or involved in all aspects of social policy and development at local, national, regional and global levels. It will be of particular interest to people with an interest in poverty and inequality, ignorance, illiteracy and unemployment, communal and familiar problems and disease in Africa and other developing country contexts. The programme will train personnel that will play key roles in Governmrnts, especially those that are in the process of democratization through devolution, decentralization and democratization such as Kenya

